local P = {}
addr = P

GAME_VERSION = 7

local RAM_BASE = 0x02000000

-- 0,0 is upper left
-- FE6 + 0x11B0 = FE7, FE7 + 0xF8 = FE8
P.CURSOR_X = {0x2AA1C, 0x2BBCC, 0x2BCC4} -- also at +4
P.CURSOR_X = RAM_BASE + P.CURSOR_X[GAME_VERSION - 5]
P.CURSOR_Y = P.CURSOR_X + 2

P.FOG = {0x2AA55, 0x2BC05, 0x2BCFD} -- +0x39 from CURSOR_X ?
P.FOG = RAM_BASE + P.FOG[GAME_VERSION - 5]
P.CHAPTER = P.FOG + 1 -- FE6x chapters count from 32
P.PHASE   = P.FOG + 2
P.TURN    = P.FOG + 3


-- FE6 + 0x11DC = FE7, FE7 + 0xFC = FE8
P.ATTACKER_NAME_CODE  = {0x39214, 0x3A3F0, 0x3A4EC} -- 2 bytes
P.ATTACKER_NAME_CODE  = RAM_BASE + P.ATTACKER_NAME_CODE[GAME_VERSION - 5]
P.ATTACKER_CLASS_CODE = P.ATTACKER_NAME_CODE +  4 -- 2 bytes
P.ATTACKER_LEVEL      = P.ATTACKER_NAME_CODE +  8 -- {0x3921C, 0x3A3F8, 0x3A4F4}
P.ATTACKER_EXP        = P.ATTACKER_NAME_CODE +  9 -- {0x3921D, 0x3A3F9, 0x3A4F5}
P.ATTACKER_SLOT_ID    = P.ATTACKER_NAME_CODE + 11 -- index of data source
P.ATTACKER_X = {0x39222, 0x3A400, 0x3A4FC} -- + 14,16,16
P.ATTACKER_X = RAM_BASE + P.ATTACKER_X[GAME_VERSION - 5]
P.ATTACKER_Y = P.ATTACKER_X + 1


P.ATTACKER_MAX_HP     = P.ATTACKER_NAME_CODE + 18 -- {0x39224, 0x3A402, 0x3A4FE}
-- next is current hp, then 6 other stats on stat screen
-- note "current" hp may be POST COMBAT hp on combat preview....
P.ATTACKER_ITEMS      = P.ATTACKER_MAX_HP + 12    -- {0x39230, 0x3A40E, 0x3A50A} 
-- weapons list in 10 bytes, (item,uses) x5
P.ATTACKER_ATK        = P.ATTACKER_MAX_HP + 0x48  -- {0x3926C, 0x3A44A, 0x3A546} 
P.ATTACKER_DEF        = P.ATTACKER_ATK +  2       -- {0x3926E, 0x3A44C, 0x3A548} 
P.ATTACKER_AS         = P.ATTACKER_ATK +  4       -- {0x39270, 0x3A44E, 0x3A54A} 
P.ATTACKER_HIT        = P.ATTACKER_ATK + 10       -- {0x39276, 0x3A454, 0x3A550} 
-- redundant with stat screen values
-- P.ATTACKER_LUCK    = P.ATTACKER_ATK + 14       -- {0x3927A, 0x3A458, 0x3A554}
P.ATTACKER_CRIT       = P.ATTACKER_ATK + 16       -- {0x3927C, 0x3A45A, 0x3A556}
-- P.ATTACKER_LEVEL2  = {0x39280, 0x3A460, 0x3A55C}
-- P.ATTACKER_EXP2    = P.ATTACKER_LEVEL2 + 1
-- NOT redundant with stat screen, that address may show post combat hp
P.ATTACKER_CURR_HP    = {0x39282, 0x3A462, 0x3A55E}
P.ATTACKER_CURR_HP = RAM_BASE + P.ATTACKER_CURR_HP[GAME_VERSION - 5]

-- 8 consecutive bytes 26, 28, 28
P.ATTACKER_RANKS = {0x3923A, 0x3A418, 0x3A514}
P.ATTACKER_RANKS = RAM_BASE + P.ATTACKER_RANKS[GAME_VERSION - 5]

local DEFENDER_OFFSET = {0x7C, 0x80, 0x80}
DEFENDER_OFFSET       = DEFENDER_OFFSET[GAME_VERSION - 5]

P.DEFENDER_NAME_CODE  = DEFENDER_OFFSET + P.ATTACKER_NAME_CODE
P.DEFENDER_CLASS_CODE = DEFENDER_OFFSET + P.ATTACKER_CLASS_CODE
P.DEFENDER_SLOT_ID    = DEFENDER_OFFSET + P.ATTACKER_SLOT_ID


return P